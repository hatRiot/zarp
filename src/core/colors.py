class color:
    """ Define plain colors
    """
    GRAY     = '\033[30m'
    RED      = '\033[31m'
    GREEN    = '\033[32m'
    YELLOW   = '\033[33m'
    BLUE     = '\033[34m'
    MAGENTA  = '\033[35m'
    CYAN     = '\033[36m'
    WHITE    = '\033[37m'
    CRIMSON  = '\033[38m'

    """ Define highlighted colors
    """
    RED_HL     = '\033[41m'
    GREEN_HL   = '\033[42m'
    BROWN_HL   = '\033[43m'
    BLUE_HL    = '\033[44m'
    MAGENTA_HL = '\033[45m'
    CYAN_HL    = '\033[46m'
    GRAY_HL    = '\033[47m'
    WHITE_HL   = '\033[48m'

    """ Define bold colors
    """
    B_GRAY     = '\033[1;30m'
    B_RED      = '\033[1;31m'
    B_GREEN    = '\033[1;32m'
    B_YELLOW   = '\033[1;33m'
    B_BLUE     = '\033[1;34m'
    B_MAGENTA  = '\033[1;35m'
    B_CYAN     = '\033[1;36m'
    B_WHITE    = '\033[1;37m'
    B_CRIMSON  = '\033[1;38m'

    """ Define bold highlighted colors
    """
    B_RED_HL     = '\033[1;41m'
    B_GREEN_HL   = '\033[1;42m'
    B_BROWN_HL   = '\033[1;43m'
    B_BLUE_HL    = '\033[1;44m'
    B_MAGENTA_HL = '\033[1;45m'
    B_CYAN_HL    = '\033[1;46m'
    B_GRAY_HL    = '\033[1;47m'
    B_WHITE_HL   = '\033[1;48m'

    """ Define end variable to cancel colors
    """
    END = '\033[0m'
